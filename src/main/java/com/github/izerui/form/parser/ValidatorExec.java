package com.github.izerui.form.parser;

/**
 * form配置的验证器
 * Created by serv on 2016/12/5.
 */
public class ValidatorExec {

    private String name;
    private String var;
    private String msg;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getVar() {
        return var;
    }

    public void setVar(String var) {
        this.var = var;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
}
